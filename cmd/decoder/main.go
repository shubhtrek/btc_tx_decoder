package main

import (
	"fmt"

	"github.com/shubhtrek/btc_tx_decoder/internal/decoder"
)


func main() {

	fmt.Println("Bitcoin Transaction Decoder")

	// raw transaction bytes (dummy for now)
	raw := []byte{
		// version
	0x01, 0x00, 0x00, 0x00,

	// input count = 1
	0x01,

	// PrevTxID (32 bytes fake)
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
	0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,

	// PrevIndex (4 bytes)
	0x00, 0x00, 0x00, 0x00,

	// Script length = 0
	0x00,

	// Sequence (4 bytes)
	0xff, 0xff, 0xff, 0xff,
	}

	tx, err := decoder.Decode(raw)
	if err != nil {
		fmt.Println("Error:", err)
		return
	}

	decoder.PrettyPrint(tx)

	

	decoder.PrettyPrint(tx)

	fmt.Println("Transaction Decoder Done")
}
